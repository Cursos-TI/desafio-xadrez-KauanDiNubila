#include <stdio.h>


int main() {
    
    //Movimento da Torre
    for (int i = 0; i < 5; i++) {
        printf("Direita\n");
    }

    //Movimento do Bispo
    int i = 0;
    while (i < 5) {
        printf("Cima, Direita\n");
        i++;
    }

    //Movimento da Rainha
    int z = 0;
    do {
        printf("Esquerda\n");
        z++;
    } while (z < 5); {
}
    


    

    return 0;
}
